#include<iostream>

using namespace std;

int main() {

	char ch; // variable declaration
	ch = 'A'; // variable assignment
	cout << ch << endl;

	int x;
	x = 500;
	cout << x << endl;

	double y;
	y = 3.14;
	cout << y << endl;

	int z;
	z = 10 + 20;
	cout << z << endl;

	int w = 0; // variable initialisation
	cout << w << endl;

	int j = 1; // variable initialisation
	cout << j << endl;

	int i = 'A'; // implicit type-casting
	cout << i << endl;

	return 0;
}